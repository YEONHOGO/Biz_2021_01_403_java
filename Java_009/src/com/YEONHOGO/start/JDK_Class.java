package com.YEONHOGO.start;

public class JDK_Class {

	public static void main(String[] args) {
		
		// 정수형변수(2의 32승)
		int intNum1 = 0;
		Integer intNum2 = 0;
		Integer intNum3 = new Integer(0);
		
		// 정수형변수(큰값, 2의 64승)
		long longNum1 = 0L;
		Long longNum2 = 0L;
		Long longNum3 = new Long(0);
		
		// 실수형변수
		float fNum1 = 0.0f;
		Float fNum2 = 0.0f;
		Float fNum3 = new Float(0.0f);
		
		// 문자 열 형 변수
		String str1 = "Korea";
		String str2 = new String("대한민국");
		
	} // main end
} // class end
